# Rule                                                                                  Example                                  
#1  Variable names must start with a letter (A-Z or a-z)** or an **underscore (\_)**\*  ✅ `name`, `_value`                           |
#2  The rest of the name can contain letters, digits (0-9)**, and **underscores** ,`count_1`, `my_var2` etc                       |
# 3 variable name can not start with digit                                        ❌ `2cool`                                    |
#  Variable names **cannot contain special characters or spaces**     
#like;-- `user-name`, `first name`   ❌               
#  Variable names are **case-sensitive**                                           
# like;-`NAME`, and `name` are all different 
